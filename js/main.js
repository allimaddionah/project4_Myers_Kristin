//Display new paragraph when Pug 1 is clicked
var mlink = document.getElementById("pug1"),
    mdiv = document.getElementById("morediv1");

    mlink.addEventListener("click", function(event) {
      event.preventDefault();
      mdiv.style.display = (mdiv.style.display === "none") ? "block" :
      "none";
    }, false
  );

  mdiv.style.display = "none";

